# One-Liner to install node modules in root folder and all subfolders of a MTA Project
# Run this script or directly the line in the root folder of your MTA project to install node modules in all subfolders with a package.json file. It will also install node modules in the root folder if a package.json file is present.

Write-Host "npm i..." -ForegroundColor Yellow;$a = Get-Item .;Write-Host "Root";npm i *>$null; dir app -Directory | ForEach-Object { Write-Host $_.Name; cd $_; npm i *>$null }; cd $a

